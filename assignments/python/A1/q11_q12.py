# 11. WAP to Find the area and circumference of circle. Formulae := area = pi*r^2, circumference = 2pi*r
# 12. WAP to Find the volume of sphere. Formula := 4/3 * pi * r^2
 
pi = 3.142
radius = 5

area = pi * (radius ** 2)
circumference = 2 * pi * radius

volume = (4/3) * pi * (radius ** 2)

print("Area of circle = ",area)
print("Circumference of circle = ",circumference)
print("Volume of sphere = ",volume)
 
 
 
 
 
 
 
 
 
 
 
 
 